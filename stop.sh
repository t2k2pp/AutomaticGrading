#!/bin/bash

# ========================================
# PM試験AI採点システム - 停止スクリプト
# ========================================

echo "================================================"
echo "PM試験AI採点システム - システム停止"
echo "================================================"

GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m'

echo -e "${YELLOW}システムを停止中...${NC}"

# Docker Composeで停止
docker-compose down

echo -e "${GREEN}✓ システムを停止しました${NC}"

# オプション: ボリュームも削除する場合
read -p "データベースのデータも削除しますか？ (y/N): " -n 1 -r
echo
if [[ $REPLY =~ ^[Yy]$ ]]; then
    echo -e "${YELLOW}データを削除中...${NC}"
    docker-compose down -v
    echo -e "${GREEN}✓ データも削除しました${NC}"
fi